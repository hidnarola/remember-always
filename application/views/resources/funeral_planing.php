            var url = '';
            if (location != '') {
                url += '?location=' + location.replace('::', ',');
            }
            if (category != '') {
                if (url != '')
                    url += '&category=' + category;
                else
                    url += '?category=' + category;
            }
            window.location.href = site_url + 'service_provider' + url;
